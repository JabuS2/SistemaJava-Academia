    public void exibirUsuario(ClienteModel cliente) {
        txtNomeCli.setText(cliente.getNomecli());
        txtTelCli.setText(cliente.getFonecli());
        txtEndCli.setText(cliente.getEndcli());
        txtEmaiCli.setText(cliente.getEmailcli());

    }

    public void limparCampos() {
        txtNomeCli.setText(null);
        txtTelCli.setText(null);
        txtEndCli.setText(null);
        txtEmaiCli.setText(null);
    }

    public void pesquisar_cliente() {
        String sql = "select * from tbclientes where nomecli like ?";
        try {
            pst = conexao.prepareStatement(sql);

            pst.setString(1, txtCliPesquisar.getText() + "%");
            rs = pst.executeQuery();
            tblClientes.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);

        }
    }

    public void setar_campos() {
        int setar = tblClientes.getSelectedRow();
        txtIdCli.setText(tblClientes.getModel().getValueAt(setar, 0).toString());
        txtNomeCli.setText(tblClientes.getModel().getValueAt(setar, 1).toString());
        txtEndCli.setText(tblClientes.getModel().getValueAt(setar, 2).toString());
        txtTelCli.setText(tblClientes.getModel().getValueAt(setar, 3).toString());
        txtEmaiCli.setText(tblClientes.getModel().getValueAt(setar, 4).toString());
        
        btnAdicionar.setEnabled(false);

    }

